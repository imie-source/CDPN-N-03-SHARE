package metier;

public class Case {
	private Integer idSol;
	private Integer idElement;
	
	
	public Case() {
		this.idSol= 0;
		this.idElement=idElement;
	}
	
	public Case(Integer idSol, Integer idElement) {
		this.idSol= idSol;
		this.idElement=idElement;
	}
	
	public Integer getIdSol() {
		return idSol;
	}
	public void setIdSol(Integer idSol) {
		this.idSol = idSol;
	}
	public Integer getIdElement() {
		return idElement;
	}
	public void setIdElement(Integer idElement) {
		this.idElement = idElement;
	}
	
	
	
	

}
